package dominio;

public class Archivo {
	
	String Nombre;
	Integer Tamanio;
	
	public String nombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public Integer tamanio() {
		return Tamanio;
	}
	public void setTamanio(Integer tamanio) {
		Tamanio = tamanio;
	}
	
}
